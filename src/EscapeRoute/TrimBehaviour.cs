namespace JackWFinlay.EscapeRoute
{
    /// <summary>
    /// Specifies trim behaviour.
    /// </summary>
    public enum TrimBehaviour
    {
        /// <summary>
        /// Trims start of string.
        /// </summary>
        None = 0,

        /// <summary>
        /// Trims start of string.
        /// </summary>
        Start = 1,

        /// <summary>
        /// Trims end of string.
        /// </summary>
        End = 2,

        /// <summary>
        /// Trims both start and end of string.
        /// </summary>
        Both = 3,
    }
}